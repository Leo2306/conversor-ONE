    private void btnConvTempActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvTempActionPerformed
        // TODO add your handling code here:
        
//            Para convertir de ºF a K use la fórmula: K = 5/9 (ºF – 32) + 273.15.
        
        if (( valorTemp.getText()).isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Ingrese un valor", "Error de validacion", JOptionPane.ERROR_MESSAGE);
        } else {
            
            if (((valorTemp.getText()).matches("^\\d+(.\\d{1,2})?$"))) {
                
                double cantidadIngresada = Double.parseDouble(valorTemp.getText());
                double valorCambiado = 0.0;
                boolean seleccion = true;
                
                switch(tempOption.getSelectedItem().toString()) {
                    case "Celsius a Fahrenheit": valorCambiado = (cantidadIngresada * 1.8) + 32; 
                        break;
                    case "Celsius a Kelvin": valorCambiado = cantidadIngresada + 273.15;
                        break;
                    case "Kelvin a Fahrenheit": valorCambiado = 1.8*(cantidadIngresada - 273.15) + 32;
                        break;
                    case "Kelvin a Celsius": valorCambiado = cantidadIngresada - 273.15;
                        break;
                    case "Fahrenheit a Celsius": valorCambiado = (cantidadIngresada - 32) / 1.8;
                        break;
                    case "Fahrenheit a Kelvin": valorCambiado = 5/9 * (cantidadIngresada - 32) + 273.15;
                        break;
                    default:
                        seleccion = false;
                }
                
                if (seleccion) {
                    String cantidadConvertida = String.format("%.5f", valorCambiado);
                    resTemp.setText(cantidadConvertida);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una opción", "Error de validacion", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
            JOptionPane.showMessageDialog(rootPane, "El valor ingresado debe coincidir con el formato solicitado", "Error de validacion", JOptionPane.ERROR_MESSAGE);
            }
        }                         
    }//GEN-LAST:event_btnConvTempActionPerformed

