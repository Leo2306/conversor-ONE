    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:   
        
        double dolar = 123.662647;
        double euro = 130.297663;
        double libra = 151.715896;
        double yen = 0.907179;
        double won = 0.095712;

        if ((valorIngresado.getText()).isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Ingrese un valor", "Error de validacion", JOptionPane.ERROR_MESSAGE);
        } else {
            
            if (((valorIngresado.getText()).matches("^\\d+(.\\d{1,2})?$"))) {
                
                double cantidadIngresada = Double.parseDouble(valorIngresado.getText());
                double valorCambiado = 0.0;
                boolean seleccion = true;
                
                switch(options.getSelectedItem().toString()) {
                    case "Pesos a Dolares": valorCambiado = cantidadIngresada / dolar; 
                        break;
                    case "Pesos a Euros": valorCambiado = cantidadIngresada / euro;
                        break;
                    case "Pesos a Libras Esterlinas": valorCambiado = cantidadIngresada / libra;
                        break;
                    case "Pesos a Yen": valorCambiado = cantidadIngresada / yen;
                        break;
                    case "Pesos a Won": valorCambiado = cantidadIngresada / won;
                        break;
                    case "Dolares a Pesos": valorCambiado = cantidadIngresada * dolar;
                        break;
                    case "Euros a Pesos": valorCambiado = cantidadIngresada * euro;
                        break;
                    case "Libras Esterlinas a Pesos": valorCambiado = cantidadIngresada * libra;
                        break;
                    case "Yen a Pesos": valorCambiado = cantidadIngresada * yen;
                        break;
                    case "Won a Pesos": valorCambiado = cantidadIngresada * won;
                        break;
                    default:
                        seleccion = false;
                }

                if (seleccion) {
                    String cantidadConvertida = String.format("%.5f", valorCambiado);
                    cantidadRes.setText(cantidadConvertida);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una opción", "Error de validacion", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
            JOptionPane.showMessageDialog(rootPane, "El valor ingresado debe coincidir con el formato solicitado", "Error de validacion", JOptionPane.ERROR_MESSAGE);
            }
        }          
    }//GEN-LAST:event_jButton1ActionPerformed

